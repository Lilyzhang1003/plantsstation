1喷雾法。找一个简单的带喷嘴的瓶子，装入柠檬汁和相同分量的水。喷在皮肤上，避开有伤口的地方。<br/>
2擦拭法。用柠檬皮擦拭皮肤，不仅可以清洁皮肤而且如果持之以恒还可以起到美白皮肤的功效。<br/>

3制作柠檬洁面乳，去角质乳以及面膜。将水，柠檬汁，蜂蜜和芦荟按照相同比例混合后，每天早晨用来洗脸。<br/>
美白面膜的做法：将1汤匙柠檬汁，1汤匙番茄汁，1汤匙黄瓜汁，以及1汤匙檀香木香膏混合在一起搅拌成浆糊状，然后均匀地敷在脸上，15到20分钟后用温水冲洗干净后，涂上保湿乳。<br/>
针对干燥性皮肤的面膜制作方法：将1汤匙奶粉，1汤匙蜂蜜，1汤匙柠檬汁以及2滴精油混合在一起，然后均匀地敷在脸上，15到20分钟后用温水冲洗干净后，涂上保湿乳。<br/>
柠檬去角质乳的制作方法：将2汤匙红糖，1个鸡蛋的蛋清，1汤匙柠檬汁混合在一起。双手取一些混合物然后再皮肤上打圈，进行按摩。轻轻按摩10到15分钟后，用温水冲洗干净，再抹上保湿乳。<br/>

4将柠檬切成片，在水里稍微浸几秒钟，然后挤在一个棉球上，用棉球将柠檬轻轻地擦拭在面部，停留10到30分钟后清洗干净。<br/>

